package com.examenjava.stack.domain.entity;

public class Stack {
    private int id;
    private String name;
    public Stack() {
    }
    public Stack(int id, String name) {
        this.id = id;
        this.name = name;
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    
}
